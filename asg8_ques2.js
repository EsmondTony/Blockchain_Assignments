// Write the syntax for
// a) Creating new set object
// b) Adding new element to set object
// c) Deleting element from set object

// Ans

// a) You can create a JavaScript Set by:

// Passing an Array to new Set()
// ex: let letters = new Set(["a","b","c"]);

// b) add() method of set object adds a new element to the Set
// syntax: set1.add(val);

// c) delete() method of set object deletes the specified element to the Set
// syntax: set1.delete(val);